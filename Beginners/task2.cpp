#include <iostream>

int main()
{
	int Int = 78124;
	float Float = 2.3;
	double Double = 25.369;
	char Char = 'A';
	bool Bool = true;

	std::cout << "Int = " << Int << ";\n"
			  << "Double = " << Double << ";\n"
			  << "Float = " << Float << ";\n"
			  << "Char = " << Char << ";\n"
			  << "Bool = " << Bool << "." << std::endl;

	return 0;
}
